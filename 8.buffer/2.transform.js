console.log(parseInt("e7",16)); // 16进制转10进制
console.log(parseInt("777",8)); // 8进制转10进制
console.log(parseInt("00111001",2)); // 2进制转10进制

console.log((100).toString(2)); //把100进制转成2进制
console.log((100).toString(8)); //把100进制转成8进制
console.log((100).toString(16)); //把100进制转成16进制



